# The keyword def tells Python that we are defining a function. 
# This function is called greet_customer. Everything that is 
# indented after the : is what is run when greet_customer() 
# is called. So every time we call greet_customer(), the three print statements run.
def greet_customer():
  print("Welcome to Engrossing Grocer's.")
  print("Our special is mandarin oranges.")
  print("Have fun shopping!")

greet_customer()
# prints greeting lines

