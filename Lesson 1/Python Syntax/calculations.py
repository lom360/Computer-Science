# Python performs addition, subtraction, multiplication, and division with +, -, *, and /.

# Prints "500"
print(573 - 74 + 1)

# Prints "50"
print(25 * 2)

# Prints "2.0"
print(10 / 5)
# Notice that when we perform division, the result has a decimal place. 
# This is because Python converts all ints to floats before performing division.

# Mathematical operations in Python follow the standard mathematical order of operations.
print(25 * 68 + 13 / 28)
